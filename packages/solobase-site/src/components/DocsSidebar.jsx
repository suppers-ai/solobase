export default function DocsSidebar({ currentPath }) {
  return (
    <aside class="w-64 bg-white border-r border-gray-200 docs-sidebar overflow-y-auto hidden lg:block">
      <div class="p-6">
        <nav class="space-y-1">
          {docsSidebar.map((section) => (
            <div key={section.title} class="mb-4">
              <h3 class="text-xs font-semibold text-gray-500 uppercase tracking-wider mb-2">{section.title}</h3>
              <ul class="space-y-1">
                {section.items.map((item) => {
                  const isActive = currentPath === item.path;
                  const iconPath = sidebarIcons[item.name];
                  return (
                    <li key={item.path}>
                      <a
                        href={`/docs/${item.path === '/docs/' ? '' : '#' + item.path.replace('/docs/', '').replace(/\/$/, '')}`}
                        onClick={(e) => {
                          e.preventDefault();
                          // Use history API to change the path and trigger re-render
                          window.history.pushState({}, '', item.path);
                          window.dispatchEvent(new PopStateEvent('popstate'));
                        }}
                        class={`flex items-center px-2 py-1 text-sm rounded hover:bg-gray-100 ${
                          isActive ? 'bg-blue-50 text-blue-700 border-r-2 border-blue-500' : 'text-gray-700'
                        }`}
                      >
                        {iconPath && (
                          <svg class="mr-2 h-4 w-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d={iconPath} />
                          </svg>
                        )}
                        {item.name}
                      </a>
                    </li>
                  );
                })}
              </ul>
            </div>
          ))}
        </nav>
      </div>
    </aside>
  );
}
